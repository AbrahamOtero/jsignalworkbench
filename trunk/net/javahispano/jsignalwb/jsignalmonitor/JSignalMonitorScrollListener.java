package net.javahispano.jsignalwb.jsignalmonitor;

/**
 * Interfaz que debe incrementar aquella clase interesada en ser notificada de
 * eventos de cambio de valor de la scroll de JSignalMonitor.
 *
 * @author This software is under the Apache License Version 2.0
 *   (http://www.apache.org/licenses/). Copyright 2006-2007 Román Segador y
 *   Abraham Otero
 */
interface JSignalMonitorScrollListener {

    /**
     * Se invoca cuando el scroll de JSignalMonitor cambia de valor.
     *
     * @param evt {@link ScrollValueChangeEvent}
     */
    public void scrollValueChanged(JSignalMonitorScrollEvent evt);

}
